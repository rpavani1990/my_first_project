package javaexamples;

public class Stringmethodspractice {
	public static void main (String args []) {
	// Arithmetic operators examples 
		int a=20;
		int b=30;
		
		System.out.println(a+b);//50   addition operator
		System.out.println(a-b);//-10  subtraction operator
		System.out.println(a*b);//600  multiplication operator 
		System.out.println(a%b);//20   modulus operator
		System.out.println(a/b);//0    division operator
	}
	
	
	
	
	

}
